<?php


namespace App\Schema;


/**
 * Class ItemsCategory
 * @package App\Schema
 */
class ItemsCategory
{
    const TABLENAME = "items_cat";
    private string $id;
    public string $title;

    /**
     * @return string
     */
    public function getId(): string
    {
        return $this->id;
    }

    /**
     * @param string $id
     */
    public function setId(string $id): void
    {
        $this->id = $id;
    }

    /**
     * @return string
     */
    public function getTitle(): string
    {
        return $this->title;
    }

    /**
     * @param string $title
     */
    public function setTitle(string $title): void
    {
        $this->title = $title;
    }
}